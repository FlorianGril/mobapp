package com.example.bankapp;

public class Point {
    String id;
    String name;
    String lastname;

    public String getId() {
        return id;
    }
    public void setId(String id) {
        id = id;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        name = name;
    }
    public String getLastname() {
        return lastname;
    }
    public void setLastname(String lastname) {
        lastname = lastname;
    }
}
